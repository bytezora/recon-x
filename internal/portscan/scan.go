func Scan(subs []subdomain.Result, ports []int, threads int, onFound func(Result)) []Result {
	results := make([]Result, 0, 64)
	mu := sync.Mutex{}
	sem := make(chan struct{}, threads)
	wg := sync.WaitGroup{}

	for _, sub := range subs {
		for _, port := range ports {
			for _, ip := range sub.IPs {
				sem <- struct{}{}
				wg.Add(1)
				go func(host, addr string, p int) {
					defer func() { <-sem; wg.Done() }()
					conn, err := net.DialTimeout("tcp", fmt.Sprintf("%s:%d", addr, p), dialTimeout)
					if err != nil {
						return
					}
					bannerStr := banner.GrabConn(conn, p)
					conn.Close()
					svc := serviceNames[p]
					if svc == "" {
						svc = banner.GuessService(bannerStr, p)
					}
					r := Result{
						Host:    host,
						Port:    p,
						IP:      addr,
						Banner:  bannerStr,
						Service: svc,
						State:   "open",
					}
					mu.Lock()
					results = append(results, r)
					mu.Unlock()
					if onFound != nil {
						onFound(r)
					}
				}(sub.Subdomain, ip, port)
			}
		}
	}
	wg.Wait()
	return results
}

func ScanIP(ip string, ports []int, threads int, onFound func(Result)) []Result {
	fakeResult := subdomain.Result{Subdomain: ip, IPs: []string{ip}}
	return Scan([]subdomain.Result{fakeResult}, ports, threads, onFound)
}

func ScanCIDR(cidr string, ports []int, threads int, onFound func(Result)) []Result {
	ips, err := expandCIDR(cidr)
	if err != nil || len(ips) == 0 {
		return nil
	}
	var all []Result
	mu := sync.Mutex{}
	wg := sync.WaitGroup{}
	sem := make(chan struct{}, 5)
	for _, ip := range ips {
		sem <- struct{}{}
		wg.Add(1)
		go func(addr string) {
			defer func() { <-sem; wg.Done() }()
			res := ScanIP(addr, ports, threads, onFound)
			mu.Lock()
			all = append(all, res...)
			mu.Unlock()
		}(ip)
	}
	wg.Wait()
	return all
}

func expandCIDR(cidr string) ([]string, error) {
	ip, ipnet, err := net.ParseCIDR(cidr)
	if err != nil {
		return nil, err
	}
	var ips []string
	for ip2 := ip.Mask(ipnet.Mask); ipnet.Contains(ip2); increment(ip2) {
		ips = append(ips, ip2.String())
		if len(ips) > 65536 {
			break
		}
	}
	if len(ips) > 2 {
		ips = ips[1 : len(ips)-1]
	}
	return ips, nil
}

func increment(ip net.IP) {
	for j := len(ip) - 1; j >= 0; j-- {
		ip[j]++
		if ip[j] > 0 {
			break
		}
	}
}
